
package funcionesjava;
public class MayorMenorIgual {
    void mayorMenorIgual(float x, float y){
        if(x<y) 
            System.out.println(x+" es menor que "+y);
        else if(x>y)
            System.out.println(x+" es mayor que "+y);
        else   
            System.out.println(x+" es igual a "+y);
    }
}
